//****************************************************************************************************************************************************************
// 名称               : SPI_Send_8Bit()
// 创建日期           : 2017-05-09
// 作者               : 庄明群
// 功能               : SPI通信发送一字节数据
// 输入参数           : sBit8(待发送字节)
// 输出参数           : 无
// 返回结果           : 无
// 注意和说明         : 
// 修改内容           :
//****************************************************************************************************************************************************************

void SPI_Send_8Bit(uint8_t sBit8)
{
  uint8_t si;
  uint8_t Bit8;
  Bit8 = sBit8;
  
  for(si = 0; si < 8; si++)
  {
    if(Bit8 & 0x80)
    {
      SPI_Bit_Set();
    }
    else
    {
      SPI_Bit_Clr();
    }
    
    Bit8 <<= 1;
  }
}

