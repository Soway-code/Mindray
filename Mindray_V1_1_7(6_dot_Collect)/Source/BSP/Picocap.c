//****************************************************************************************************************************************************************
// 名称               : PCap_Init()
// 创建日期           : 2017-05-13
// 作者               : 庄明群
// 功能               : PCap芯片初始化
// 输入参数           : 无
// 输出参数           : 无
// 返回结果           : 无
// 注意和说明         : 
// 修改内容           :
//****************************************************************************************************************************************************************

void PCap_Init(void)
{
  //写PCap SRAM错误
  uint8_t WrSramErr;
  //写PCap 次数
  uint8_t WrSramCnt;
  
  WrSramErr = 0;
  WrSramCnt = 0;
  
  //SPI初始化
  SPI_Init();
  //PCap复位
  PCap_Reset();
  //延时1ms
  Delay_us(1000);
  //写PCap固件
  do
  {
    WrSramErr = PCap_WR_SRAM();
    if(OP_SUCCESS == WrSramErr)
    {
      break;
    }
    
    PCap_Reset();
    WrSramCnt++;
    Delay_us(100);
  }while(WRITE_PCAPFIRM_ERR_MAX > WrSramCnt);
  
  Delay_us(100);
  //PCap寄存器配置
  PCap_Reg_Config();
  //PCap使能
  PCap_Enable();
  Delay_us(100);
  //PCap部分复位
  PCap_PTL_Reset();
  Delay_us(200);
  //PCap测量开始
  PCap_Measure();
}

//****************************************************************************************************************************************************************
// 名称               : PCap_Reset()
// 创建日期           : 2017-05-13
// 作者               : 庄明群
// 功能               : PCap芯片复位
// 输入参数           : 无
// 输出参数           : 无
// 返回结果           : 无
// 注意和说明         : 
// 修改内容           :
//****************************************************************************************************************************************************************

void PCap_Reset(void)
{
  SPI_ENABLE;
  Delay_us(1);
  SPI_Send_8Bit(0x88);
  SPI_DISABLE;
  Delay_us(1);
}

