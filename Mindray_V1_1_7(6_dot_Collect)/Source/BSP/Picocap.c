void PCap_Disable(void)
{
  SPI_ENABLE;
  Delay_us(1);
  SPI_Send_32Bit(0xD4000000);
  SPI_DISABLE;
  Delay_us(1);
}

//****************************************************************************************************************************************************************
// 名称               : PCap_WR_SRAM()
// 创建日期           : 2017-05-13
// 作者               : 庄明群
// 功能               : 向PCap芯片写入固件
// 输入参数           : 无
// 输出参数           : 无
// 返回结果           : OP_SUCCESS(写入成功),OP_FAILED(写入失败)
// 注意和说明         : 
// 修改内容           :
//****************************************************************************************************************************************************************

uint8_t PCap_WR_SRAM(void)
{
  uint8_t rddata;
  uint8_t errcnt;
  uint8_t *pWrdata;
  uint16_t pCnt;
  
  rddata = 0;
  errcnt = 0;
  pWrdata = (uint8_t *)SRAM_DATA;
  
  for(pCnt = 0; pCnt < SRAM_DATA_NUM; pCnt++)
  {
    do
    {
      SPI_ENABLE;
      Delay_us(1);
      SPI_Send_16Bit(pCnt | 0x9000);
      SPI_Send_8Bit(*pWrdata);
      SPI_DISABLE;
    
      Delay_us(30);
    
      SPI_ENABLE;
      Delay_us(1);
      SPI_Send_16Bit(pCnt | 0x1000);
      rddata = SPI_Receive_8Bit();
      SPI_DISABLE;
      Delay_us(1); 
    
      errcnt++;
      
      if(WRITE_PCAPFIRM_ERR_MAX < errcnt)
      {
        return OP_FAILED;
      }
      
    }while(*pWrdata != rddata);
    
    errcnt = 0;
    pWrdata++;
  }
  
  return OP_SUCCESS;
}

//****************************************************************************************************************************************************************
// 名称               : PCap_Reg_Config()
// 创建日期           : 2017-05-13
// 作者               : 庄明群
// 功能               : 向PCap芯片写入配置信息
// 输入参数           : 无
// 输出参数           : 无
// 返回结果           : 无
// 注意和说明         : 
// 修改内容           :
//****************************************************************************************************************************************************************

void PCap_Reg_Config(void)
{
  uint8_t rCnt;
  
  for(rCnt = 0; rCnt < REG_CONF_NUM; rCnt++)
  {
    SPI_ENABLE;
    Delay_us(1);
    SPI_Send_32Bit(REG[rCnt]);
    SPI_DISABLE;
    Delay_us(1);
  }
}

